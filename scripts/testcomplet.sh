set -euo pipefail

# Configuration
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(dirname "$SCRIPT_DIR")"
DATA_DIR="$PROJECT_ROOT/public/data"
CONFIG_DIR="$PROJECT_ROOT/config"
TRACKING_FILE="$CONFIG_DIR/download-tracking.json"
LOG_FILE="$DATA_DIR/logs/testcomplet-$(date +%Y%m%d-%H%M%S).log"

# Couleurs pour les logs
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# =============================================================================
# FONCTIONS UTILITAIRES
# =============================================================================

log() {
    local level="$1"
    shift
    local message="$*"
    local timestamp=$(date '+%Y-%m-%d %H:%M:%S')
    
    case "$level" in
        "INFO")  echo -e "${GREEN}[INFO]${NC} $timestamp - $message" | tee -a "$LOG_FILE" ;;
        "WARN")  echo -e "${YELLOW}[WARN]${NC} $timestamp - $message" | tee -a "$LOG_FILE" ;;
        "ERROR") echo -e "${RED}[ERROR]${NC} $timestamp - $message" | tee -a "$LOG_FILE" ;;
        "DEBUG") echo -e "${BLUE}[DEBUG]${NC} $timestamp - $message" | tee -a "$LOG_FILE" ;;
    esac
}

create_directories() {
    log "INFO" "Cr√©ation des r√©pertoires n√©cessaires..."
    mkdir -p "$DATA_DIR/logs"
    mkdir -p "$DATA_DIR/images"
    mkdir -p "$DATA_DIR/videos" 
    mkdir -p "$DATA_DIR/hls"
    mkdir -p "$CONFIG_DIR"
}

check_dependencies() {
    log "INFO" "V√©rification des d√©pendances..."
    
    local missing_deps=()
    
    if ! command -v jq &> /dev/null; then
        missing_deps+=("jq")
    fi
    
    if ! command -v ffmpeg &> /dev/null; then
        missing_deps+=("ffmpeg")
    fi
    
    if ! command -v curl &> /dev/null; then
        missing_deps+=("curl")
    fi
    
    if [ ${#missing_deps[@]} -ne 0 ]; then
        log "ERROR" "D√©pendances manquantes: ${missing_deps[*]}"
        log "ERROR" "Veuillez installer: sudo apt install ${missing_deps[*]}"
        exit 1
    fi
    
    log "INFO" "Toutes les d√©pendances sont pr√©sentes"
}

# =============================================================================
# PHASE 1: NETTOYAGE
# =============================================================================

cleanup_all_data() {
    log "INFO" "üßπ PHASE 1: Nettoyage s√©lectif (pr√©servation des images)"
    
    # Supprimer UNIQUEMENT les vid√©os et playlists (pr√©server les images)
    if [ -d "$DATA_DIR" ]; then
        log "INFO" "Pr√©servation des images existantes..."
        log "INFO" "Suppression des vid√©os..."
        find "$DATA_DIR" -name "*.mp4" -type f -delete 2>/dev/null || true
        
        log "INFO" "Suppression des playlists et segments HLS..."
        find "$DATA_DIR" -name "*.m3u8" -type f -delete 2>/dev/null || true
        find "$DATA_DIR" -name "*.ts" -type f -delete 2>/dev/null || true
        
        log "INFO" "Nettoyage des anciens logs..."
        find "$DATA_DIR/logs" -name "*.log" -type f -mtime +1 -delete 2>/dev/null || true
        
        # Supprimer les dossiers vides (sauf ceux avec images et logs)
        find "$DATA_DIR" -type d -empty -not -path "$DATA_DIR/logs*" -delete 2>/dev/null || true
    fi
    
    log "INFO" "‚úÖ Nettoyage termin√©"
}

# =============================================================================
# PHASE 2: T√âL√âCHARGEMENT DES DONN√âES (SCRIPTS DE PRODUCTION)
# =============================================================================

download_active_datasets() {
    log "INFO" "üì• PHASE 2: Ex√©cution des scripts de t√©l√©chargement de production"
    
    # V√©rifier que les scripts de production existent
    local required_scripts=(
        "$SCRIPT_DIR/smart-fetch.sh"
        "$SCRIPT_DIR/generate-historical-data.sh"
    )
    
    for script in "${required_scripts[@]}"; do
        if [ ! -f "$script" ]; then
            log "ERROR" "Script de production manquant: $script"
            exit 1
        fi
    done
    
    log "INFO" "‚úÖ Tous les scripts de production sont disponibles"
    
    # Calculer les dates pour les scripts de production (10 derniers jours, en excluant aujourd'hui)
    local end_date=$(date -d "1 day ago" +%Y-%m-%d)         # 2025-07-29 (hier)
    local start_date=$(date -d "10 days ago" +%Y-%m-%d)     # 2025-07-20 (il y a 10 jours)
    local days_count=$(( ($(date -d "$end_date" +%s) - $(date -d "$start_date" +%s)) / 86400 + 1 ))
    
    log "INFO" "üìÖ P√©riode de test: $start_date √† $end_date ($days_count jours)"
    
    # Option 1: Ex√©cuter smart-fetch.sh avec les bonnes dates (param√®tres de production)
    log "INFO" "üöÄ Lancement de smart-fetch.sh sync avec p√©riode $start_date √† $end_date (toutes les images disponibles)"
    if bash "$SCRIPT_DIR/smart-fetch.sh" sync "$days_count" 2>&1 | tee -a "$LOG_FILE"; then
        log "INFO" "‚úÖ smart-fetch.sh termin√© avec succ√®s"
    else
        log "WARN" "‚ö†Ô∏è smart-fetch.sh a rencontr√© des probl√®mes, tentative avec generate-historical-data.sh"
        
        # Option 2: Ex√©cuter generate-historical-data.sh avec les bonnes dates
        log "INFO" "üöÄ Lancement de generate-historical-data.sh avec p√©riode $start_date √† $end_date"
        if bash "$SCRIPT_DIR/generate-historical-data.sh" "$start_date" "$end_date" 2>&1 | tee -a "$LOG_FILE"; then
            log "INFO" "‚úÖ generate-historical-data.sh termin√© avec succ√®s"
        else
            log "ERROR" "‚ùå √âchec des scripts de t√©l√©chargement de production"
            return 1
        fi
    fi
    
    # V√©rifier que des donn√©es ont √©t√© t√©l√©charg√©es
    local image_count=$(find "$DATA_DIR" -name "*.jpg" -type f | wc -l)
    log "INFO" "üìä Images t√©l√©charg√©es par les scripts de production: $image_count"
    
    if [ "$image_count" -eq 0 ]; then
        log "WARN" "‚ö†Ô∏è Aucune image t√©l√©charg√©e par les scripts de production"
    fi
    
    log "INFO" "‚úÖ Phase de t√©l√©chargement de production termin√©e"
}

# =============================================================================
# PHASE 3: G√âN√âRATION DES VID√âOS (SCRIPTS DE PRODUCTION)
# =============================================================================

generate_daily_videos() {
    log "INFO" "üé¨ PHASE 3: Ex√©cution des scripts de g√©n√©ration vid√©o de production"
    
    # V√©rifier que le script de production existe
    if [ ! -f "$SCRIPT_DIR/generate-satellite-videos.sh" ]; then
        log "ERROR" "Script de production manquant: generate-satellite-videos.sh"
        exit 1
    fi
    
    # V√©rifier qu'on a des images √† traiter
    local image_count=$(find "$DATA_DIR" -name "*.jpg" -type f | wc -l)
    if [ "$image_count" -eq 0 ]; then
        log "WARN" "Aucune image trouv√©e pour g√©n√©rer des vid√©os"
        return 0
    fi
    
    log "INFO" "üìä $image_count images disponibles pour la g√©n√©ration vid√©o"
    
    # Lancer le script de production pour la g√©n√©ration de vid√©os
    log "INFO" "üé¨ Lancement de generate-satellite-videos.sh auto (g√©n√©ration automatique)"
    if bash "$SCRIPT_DIR/generate-satellite-videos.sh" auto 2>&1 | tee -a "$LOG_FILE"; then
        log "INFO" "‚úÖ generate-satellite-videos.sh termin√© avec succ√®s"
    else
        log "WARN" "‚ö†Ô∏è generate-satellite-videos.sh a rencontr√© des probl√®mes"
        
        # Fallback: utiliser generate-daily-video.sh pour chaque date trouv√©e
        log "INFO" "üîÑ Fallback: utilisation de generate-daily-video.sh"
        
        local image_dates=$(find "$DATA_DIR" -name "*.jpg" -type f | \
            grep -oE "[0-9]{4}-[0-9]{2}-[0-9]{2}" | \
            sort -u)
        
        if [ -n "$image_dates" ]; then
            echo "$image_dates" | while read -r date; do
                if [ -n "$date" ]; then
                    log "INFO" "  üé¨ G√©n√©ration vid√©o pour $date"
                    
                    # Trouver les datasets pour cette date
                    local dataset_dirs=$(find "$DATA_DIR" -path "*/$date/*.jpg" -type f | \
                        sed "s|$DATA_DIR/||" | \
                        sed 's|/[^/]*/[^/]*$||' | \
                        sort -u)
                    
                    echo "$dataset_dirs" | while read -r dataset_path; do
                        if [ -n "$dataset_path" ]; then
                            local dataset_key=$(echo "$dataset_path" | tr '/' '.')
                            log "INFO" "    üìπ $dataset_key pour $date"
                            
                            bash "$SCRIPT_DIR/generate-daily-video.sh" "$date" "$dataset_key" >> "$LOG_FILE" 2>&1 || {
                                log "WARN" "√âchec g√©n√©ration vid√©o pour $dataset_key le $date"
                            }
                        fi
                    done
                fi
            done
        fi
    fi
    
    # V√©rifier les r√©sultats
    local video_count=$(find "$DATA_DIR" -name "*.mp4" -type f | wc -l)
    log "INFO" "üìä Vid√©os g√©n√©r√©es par les scripts de production: $video_count"
    
    log "INFO" "‚úÖ Phase de g√©n√©ration vid√©o de production termin√©e"
}

# =============================================================================
# PHASE 4: CR√âATION DES PLAYLISTS
# =============================================================================

create_playlists() {
    log "INFO" "üìã PHASE 4: Cr√©ation des playlists HLS"
    
    # Les playlists sont normalement cr√©√©es par generate-daily-video.sh
    # V√©rifions qu'elles existent et comptons-les
    
    local playlist_count=$(find "$DATA_DIR/hls" -name "playlist.m3u8" -type f | wc -l)
    local segment_count=$(find "$DATA_DIR/hls" -name "*.ts" -type f | wc -l)
    
    log "INFO" "Playlists HLS cr√©√©es: $playlist_count"
    log "INFO" "Segments vid√©o cr√©√©s: $segment_count"
    
    if [ "$playlist_count" -gt 0 ]; then
        log "INFO" "Structure des playlists:"
        find "$DATA_DIR/hls" -name "playlist.m3u8" -type f | head -5 | while read -r playlist; do
            local rel_path=$(echo "$playlist" | sed "s|$DATA_DIR/||")
            log "INFO" "  - $rel_path"
        done
        
        if [ "$playlist_count" -gt 5 ]; then
            log "INFO" "  ... et $((playlist_count - 5)) autres"
        fi
    fi
    
    log "INFO" "‚úÖ Playlists HLS disponibles"
}

